export const createDefaultItemsForDeceased = (deceasedId: string) => {
  return defaultFuneralItemTemplates.map((template) => ({
    id: `${deceasedId}-item-${template.id}-${Date.now()}-${Math.random().toString(36).substr(2, 9)}`,
    name: template.name,
    category: template.category,
    description: template.description,
    quantity: template.defaultQuantity,
    unit: template.unit,
    purchased: false,
    deceasedId,
    createdAt: new Date().toISOString(),
  }));
};